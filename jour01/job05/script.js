function afficherjourssemaines() {
    // Création du tableau avec les jours de la semaine
    let jourssemaines = ["Lundi", "Mardi", "Mercredi", "Jeudi", "Vendredi", "Samedi", "Dimanche"];

    // Boucle for pour afficher chaque jour
    for (let i = 0; i < jourssemaines.length; i++) {
        console.log(jourssemaines[i]);
    }
}

// Appel de la fonction pour voir le résultat dans la console
afficherjourssemaines();
